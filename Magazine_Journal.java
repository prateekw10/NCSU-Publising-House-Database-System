
public class Magazine_Journal{
    int p_id;
    int issue;
    String periodicity;
    
    public int getP_id(){
        return p_id;
    }

    public int getIssue(){
        return issue;
    }

    public String getPeriodicity(){
        return periodicity;
    }


    public void setP_id(int p_id){
        this.p_id = p_id;
    }


    public void setIssue(int issue){
        this.issue = issue;
    }

    public void setPeriodicity(String periodicity){
        this.periodicity = periodicity;
    }

}